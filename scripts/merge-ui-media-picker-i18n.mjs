for (const loc of ['de', 'fr', 'es', 'it', 'ja', 'zh', 'ar']) {
  const p = path.join(root, 'messages', `${loc}.json`)
  const j = JSON.parse(fs.readFileSync(p, 'utf8'))
  j.ui = j.ui || {}
  j.ui.lightboxImageAlt = LIGHTBOX_ALT[loc]
  j.mediaPicker = { ...j.mediaPicker, ...MEDIA_PICKER[loc] }
  j.pinGate = { ...j.pinGate, ...PIN_GATE[loc] }
  fs.writeFileSync(p, JSON.stringify(j, null, 2) + '\n')
}
