function fmtBlock(obj, indent = '    ') {
  return Object.entries(obj)
    .map(([k, v]) => `${indent}"${k}": ${JSON.stringify(v)}`)
    .join(',\n')
}

function patchLocale(file, receipt, app, loginTenant, accLegend, accTerms, accPwd) {
  let s = fs.readFileSync(file, 'utf8')
  const re = /"kassaReceipt":\s*\{[\s\S]*?\},\s*\n\s*"staffClock"/
  const m = s.match(re)
  if (!m) throw new Error('kassaReceipt block not found: ' + file)
  const replacement = `"kassaReceipt": {\n${fmtBlock(receipt, '    ')}\n  },\n  "kassaApp": {\n${fmtBlock(app, '    ')}\n  },\n  "staffClock"`
  s = s.replace(re, replacement)

  s = s.replace(
    /("loginFailed":\s*"[^"]+",)\s*("somethingWentWrong")/,
    `$1\n    "tenantNotFound": ${JSON.stringify(loginTenant)},\n    $2`
  )

  s = s.replace(
    /("backToMenu":\s*"[^"]+")\s*\n\s*\},\s*\n\s*"reviewPage"/,
    `$1,\n    "requiredFieldLegend": ${JSON.stringify(accLegend)},\n    "registerTermsNotice": ${JSON.stringify(accTerms)},\n    "passwordPlaceholderMasked": ${JSON.stringify(accPwd)}\n  },\n  "reviewPage"`
  )

  fs.writeFileSync(file, s)
}

